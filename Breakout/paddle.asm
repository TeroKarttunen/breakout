porta = $dc00
ciddra = $dc02
sid = $d400
buffer = $02ba
pdlx = $02bb
pdly = $02bc 
btna = $02bd
btnb = $02be

read_paddle:
;1040 PORTA=$DC00
;1050 CIDDRA=$DC02
;1060 SID=$D400
;1070 *=$C100
;1080 BUFFER  *=*+1
;1090 PDLX    *=*+2
;1100 PDLY    *=*+2
;1110 BTNA    *=*+1
;1120 BTNB    *=*+1
  ldx #0        ; FOR FOUR PADDLES OR TWO ANALOG JOYSTICKS
;PDLRD0:           ; ENTRY POINT FOR ONE PAIR (CONDITION X 1ST)
;  sei
  lda ciddra    ; GET CURRENT VALUE OF DDR
  sta buffer    ; SAVE IT AWAY
  lda #$C0
  sta ciddra    ; SET PORT A FOR INPUT
  lda #$80
PDLRD1:
  sta porta     ; ADDRESS A PAIR OF PADDLES
  ldy #$80      ; WAIT A WHILE
PDLRD2:
  nop
  dey
  bpl PDLRD2
  lda sid+25    ; GET X VALUE
  sta pdlx,x
  ;sta bat_x
  stx temp_variable
  tax
  lda paddle_conversion_table1,x
  sta bat_x
  lda paddle_conversion_table2,x
  sta bat_x_overflow
  ldx temp_variable
  lda sid+26
  sta pdly,x    ; GET Y VALUE
  lda porta     ; TIME TO READ PADDLE FIRE BUTTONS
  ora #80       ; MAKE IT THE SAME AS OTHER PAIR
  sta btna      ; BIT 2 IS PDL X, BIT 3 IS PDL Y
  lda #$40
  dex           ; ALL PAIRS DONE?
  bpl PDLRD1    ; NO
  lda buffer
  sta ciddra    ; RESTORE PREVIOUS VALUE OF DDR
  lda porta+1   ; FOR 2ND PAIR -
  sta btnb      ; BIT 2 IS PDL X, BIT 3 IS PDL Y
;  cli 
  rts

